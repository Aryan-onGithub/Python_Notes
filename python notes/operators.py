num1 = 10
num2 = 5

'''Arithmatic Operators'''

print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
print("num1 ** num2 is ", num1 ** num2) #calcualte num1 to the power num2 (num1^num2)
print("num1 // num2 is ", num1 // num2) #do floor division no values after decimal
print("num1 % num2 is ", num1 % num2) # gives remainder

'''Assignment Operators'''
a = 4
a += 2 #can be done for each assignment operator

print(a)

'''Comparison Operators (<,>,!=)'''
#give answers in true and false
x = 8
y = 3
print(x>y)
print(x<y)
print(x==y) #is equal to
print(x!=y) #is not equal to

'''Logical Operators (and, or)'''
#give answer in true or false
print("a")
print(x==y and x>y) #need both true
print(x==y or x>y) #need anyone true
print(not(False)) #gives opposite of the actual answer
print(not(True))
